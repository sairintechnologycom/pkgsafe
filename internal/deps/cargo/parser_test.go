package cargo

import (
	"reflect"
	"testing"
)

func TestParseCargoLock(t *testing.T) {
	content := []byte(`# This file is automatically @generated by Cargo.
# It is not intended for manual editing.
version = 3

[[package]]
name = "addr2line"
version = "0.21.0"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "8a30b2e23b9e17a9f90641c7ab1549cd9b44f296d3ccbf309d2863cfe398a0cb"
dependencies = [
 "gimli",
]

[[package]]
name = "adler"
version = "1.0.2"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "f26201604c87b1e01bd3d98f8d5d9a8fcbb815e8cedb41ffccbeb4bf593a35fe"

[metadata]
something = "else"
`)

	expected := []Dependency{
		{Name: "addr2line", Version: "0.21.0"},
		{Name: "adler", Version: "1.0.2"},
	}

	got, err := ParseCargoLock(content)
	if err != nil {
		t.Fatalf("unexpected error parsing Cargo.lock: %v", err)
	}

	if !reflect.DeepEqual(got, expected) {
		t.Errorf("ParseCargoLock() =\n%+v\nwant\n%+v", got, expected)
	}
}
