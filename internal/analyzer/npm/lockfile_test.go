// ────────────────────────────────────────────────────────────────────────────
// pnpm v8 format (name@ver: keys without leading slash)
// ────────────────────────────────────────────────────────────────────────────

func TestAnalyzePnpmLockfile_V8Format(t *testing.T) {
	dir := t.TempDir()
	p := filepath.Join(dir, "pnpm-lock.yaml")
	content := `lockfileVersion: '9.0'

packages:
  lodash@4.17.21:
    resolution: {integrity: sha512-abc==}
  react@18.2.0:
    resolution: {integrity: sha512-def==}
`
	if err := writeFile(p, []byte(content)); err != nil {
		t.Fatal(err)
	}
	res, err := AnalyzePnpmLockfile(p, policy.Default())
	if err != nil {
		t.Fatalf("error: %v", err)
	}
	// Should parse 2 packages and not block for known-safe packages.
	found := false
	for _, s := range res.Suspicious {
		if strings.Contains(s, "total:2") {
			found = true
		}
	}
	if !found {
		t.Errorf("expected total:2 packages parsed from v8 format, got: %v", res.Suspicious)
	}
}

// ────────────────────────────────────────────────────────────────────────────
// writeFile helper (avoids import of io/ioutil)
// ────────────────────────────────────────────────────────────────────────────

func writeFile(path string, data []byte) error {
	f, err := os.Create(path)
	if err != nil {
		return err
	}
	defer f.Close()
	_, err = f.Write(data)
	return err
}
