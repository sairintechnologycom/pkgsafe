func TestAnalyzePnpmLockfile_SafePackages(t *testing.T) {
	path := filepath.Join("..", "..", "..", "testdata", "npm", "pnpm-lock.yaml")
	res, err := AnalyzePnpmLockfile(path, policy.Default())
	if err != nil {
		t.Fatalf("AnalyzePnpmLockfile error: %v", err)
	}
	if res.Decision == "BLOCK" {
		t.Errorf("expected ALLOW or WARN for safe pnpm lockfile, got BLOCK; reasons: %v", res.Reasons)
	}
}

func TestAnalyzePnpmLockfile_ParsesPackages(t *testing.T) {
	path := filepath.Join("..", "..", "..", "testdata", "npm", "pnpm-lock.yaml")
	res, err := AnalyzePnpmLockfile(path, policy.Default())
	if err != nil {
		t.Fatalf("AnalyzePnpmLockfile error: %v", err)
	}
	// The lockfile_summary suspicious entry must mention ≥3 packages.
	found := false
	for _, s := range res.Suspicious {
		if strings.HasPrefix(s, "lockfile_summary:total:") {
			// Should have total:3 for the 3 packages in the fixture.
			if strings.Contains(s, "total:3") {
				found = true
			}
		}
	}
	if !found {
		t.Errorf("expected lockfile_summary:total:3 in Suspicious, got: %v", res.Suspicious)
	}
}

func TestAnalyzePnpmLockfile_BlockedPackage(t *testing.T) {
	pol := policy.Default()
	pol.BlockedPackages.NPM = []string{"lodash"}

	path := filepath.Join("..", "..", "..", "testdata", "npm", "pnpm-lock.yaml")
	res, err := AnalyzePnpmLockfile(path, pol)
	if err != nil {
		t.Fatalf("AnalyzePnpmLockfile error: %v", err)
	}
	foundBlocked := false
	for _, r := range res.Reasons {
		if r.ID == "blocked_package" && strings.Contains(r.Evidence, "lodash") {
			foundBlocked = true
		}
	}
	if !foundBlocked {
		t.Errorf("expected blocked_package reason for lodash, got: %v", res.Reasons)
	}
}

func TestAnalyzePnpmLockfile_EmptyFile(t *testing.T) {
	// Write a minimal pnpm lockfile with zero packages.
	dir := t.TempDir()
	empty := filepath.Join(dir, "pnpm-lock.yaml")
	if err := writeFile(empty, []byte("lockfileVersion: '6.0'\n\npackages:\n")); err != nil {
		t.Fatal(err)
	}
	res, err := AnalyzePnpmLockfile(empty, policy.Default())
	if err != nil {
		t.Fatalf("AnalyzePnpmLockfile error: %v", err)
	}
	// An empty lockfile should not score above 0 and should ALLOW.
	if res.Score > 0 {
		t.Errorf("expected score 0 for empty pnpm lockfile, got %d; reasons: %v", res.Score, res.Reasons)
	}
	if res.Decision == "BLOCK" {
		t.Errorf("expected ALLOW for empty pnpm lockfile, got BLOCK")
	}
}

